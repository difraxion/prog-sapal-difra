<div id="gralBox" class="alcantarillado">
  <p class="taj">
    El sistema de alcantarillado de León cuenta con 2,816
    kilómetros lineales, de los cuales 2,684 kilómetros son de
    drenaje mixto, es decir, desaloja aguas residuales domésticas y
    de lluvias, y 132 kilómetros de alcantarillado pluvial.
  </p>
  <p class="taj">
    Por ello tiene capacidad sobrada para las aguas residuales,
    pero es insuficiente para la totalidad de las aguas pluviales.
  </p>
  <p class="taj">
    La ciudad produce 2,533 litros por segundo al día de aguas
    residuales; 1,750 litros por segundo provienen de las tomas
    abastecidas por el SAPAL, y el resto son aguas provenientes de
    pozos privados.
  </p>
  <p class="taj">
    Las aguas residuales de León se componen de aguas grises
    procedentes de los hogares y aguas negras que depositan las
    industrias asentadas en la zona urbana ocasionando un factor de
    riesgo de contaminación y reblandecimiento del terreno.
  </p>
  <p class="taj">
    El sector industrial ha realizado un gran esfuerzo y ya ha
    desincorporado de la mancha urbana el 90% de la producción
    total de cueros curtidos de la ciudad, con lo cual las
    condiciones del alcantarillado de la ciudad, han mejorado
    sustantivamente. Sapal continúa abierto a apoyar a los
    industriales que aún no han desincorporado sus procesos
    húmedos, para encontrar soluciones que beneficien a la ciudad y
    a sus empresas.
  </p>
  <h1 class="tac">
    AGUA DE LLUVIAS
  </h1>
  <div class="lrow">
    <p class="taj">
      La pendiente natural de la ciudad y por ende la orientación
      de las corrientes pluviales es del norte hacia el sureste.
      Sin embargo, los pasos naturales del agua han sido
      obstaculizados por:<br />
      - Las rasantes de bulevares y calles.<br />
      - Desvío, invasión o desaparición de arroyos.<br />
      - La deficiente nivelación de los pavimentos.
    </p>
    <p class="taj">
      Por ello en una línea prioritaria de trabajo, la prevención
      de inundaciones y encharcamientos, se creó el Plan Maestro
      Pluvial que consta de dos partes:
    </p>
    <p class="taj">
      •&nbsp;&nbsp;&nbsp;&nbsp;Identificación de los puntos
      críticos de inundación, los cuales sumaron 60 y la
      preparación de los proyectos técnicos para corregirlo. A
      diciembre de 2011 se cuentan con 43 anteproyectos para
      corregir algunos de los puntos con mayores problemas, en
      donde se aplican recursos de la Federación y el Estado, con
      el apoyo técnico y operativo de Sapal.
    </p>
    <p class="taj">
      •&nbsp;&nbsp;&nbsp;&nbsp;Elaboración del plano hidrológico de
      León, en el que están identificados los 59 arroyos que cruzan
      el municipio - 39 cruzan la ciudad- tanto agua arriba como
      agua abajo.
    </p>
